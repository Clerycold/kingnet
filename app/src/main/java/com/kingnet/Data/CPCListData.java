public class CPCListData {

    private String type;
    private String station_name;
    private String address;
    private String km;
    private String latitude;
    private String longitude;
    private String open_start;
    private String open_due;


    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStation_name() {
        return station_name;
    }

    public void setStation_name(String station_name) {
        this.station_name = station_name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
    public String getKm() {
        return km;
    }

    public void setKm(String km) {
        this.km = km;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getOpen_due() {
        return open_due;
    }

    public void setOpen_due(String open_due) {
        this.open_due = open_due;
    }

    public String getOpen_start() {
        return open_start;
    }

    public void setOpen_start(String open_start) {
        this.open_start = open_start;
    }


}
